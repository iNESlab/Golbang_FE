class MemberListPage extends ConsumerStatefulWidget {
  final int clubId;
  final bool isAdmin;
  final int initialTabIndex; // üîß Ï∂îÍ∞Ä: Ï¥àÍ∏∞ ÌÉ≠ Ïù∏Îç±Ïä§

  const MemberListPage({
    super.key,
    required this.clubId,
    required this.isAdmin,
    this.initialTabIndex = 0, // üîß Í∏∞Î≥∏Í∞í: Ï≤´ Î≤àÏß∏ ÌÉ≠
  });

  @override
  _MemberListPageState createState() => _MemberListPageState();
}

class _MemberListPageState extends ConsumerState<MemberListPage> {
  List<ClubMemberProfile> activeMembers = [];
  List<ClubMemberProfile> pendingMembers = [];
  List<GetAllUserProfile> oldMemberUsers = [];
  bool isLoading = true;
  bool isDeleteMode = false;
  late ClubMemberService _clubMemberService;
  late ClubService _clubService;

  @override
  void initState() {
    super.initState();
    _clubMemberService = ClubMemberService(ref.read(secureStorageProvider));
    _clubService = ClubService(ref.read(secureStorageProvider));
    _fetchMembers();
  }

  Future<void> _fetchMembers() async {
    try {
      final fetched = await _clubMemberService.getClubMemberProfileList(clubId: widget.clubId);
      setState(() {
        oldMemberUsers = fetched.map((m)=>m.toUserProfile()).toList();
        activeMembers = fetched.where((m) => m.statusType == 'active').toList();
        pendingMembers = fetched.where((m) => m.statusType == 'invited' || m.statusType == 'applied').toList();
        isLoading = false;
      });
    } catch (e) {
      log("Error fetching members: $e");
      setState(() => isLoading = false);
    }
  }

  @override
  Widget build(BuildContext context) {
    return DefaultTabController(
      length: widget.isAdmin ? 2 : 0,
      initialIndex: widget.initialTabIndex, // üîß Ï∂îÍ∞Ä: Ï¥àÍ∏∞ ÌÉ≠ ÏÑ§Ï†ï
      child: Scaffold(
        appBar: AppBar(
          title: const Text("Î©§Î≤Ñ Í¥ÄÎ¶¨"),
          bottom: widget.isAdmin ? TabBar(
            tabs: [
              const Tab(text: "ÌôúÎèô Î©§Î≤Ñ"),
              if (widget.isAdmin) const Tab(text: "Ï¥àÎåÄ/Ïã†Ï≤≠ ÎåÄÍ∏∞"),
            ],
          ): null,
        ),
        body: isLoading
            ? const Center(child: CircularProgressIndicator())
            : TabBarView(
          children: [
            _buildActiveList(),
            if (widget.isAdmin) _buildPendingList(),
          ],
        ),
        floatingActionButton: widget.isAdmin
            ? FloatingActionButton(
          onPressed: _showInviteDialog,
          child: const Icon(Icons.person_add),
        )
            : null,
      ),
    );
  }

  Widget _buildActiveList() {
    if (activeMembers.isEmpty) {
      return const Center(child: Text("ÌôúÎèô Î©§Î≤ÑÍ∞Ä ÏóÜÏäµÎãàÎã§."));
    }
    return ListView(
      children: activeMembers.map((m) => _buildActiveTile(m)).toList(),
    );
  }

  Widget _buildPendingList() {
    if (pendingMembers.isEmpty) {
      return const Center(child: Text("Í∞ÄÏûÖ ÎåÄÍ∏∞ Î©§Î≤ÑÍ∞Ä ÏóÜÏäµÎãàÎã§."));
    }
    return ListView(
      children: pendingMembers.map((m) => _buildPendingTile(m)).toList(),
    );
  }


  Widget _buildActiveTile(ClubMemberProfile member) {
    final isAdminMember = member.role == "admin";

    return Card(
      color: Colors.transparent,
      elevation: 0,
      shape: RoundedRectangleBorder(
        borderRadius: BorderRadius.circular(10),
      ),
      child: ListTile(
        leading: CircleAvatar(
          radius: 25,
          backgroundColor: Colors.grey[200],
          backgroundImage: (member.profileImage.isNotEmpty &&
              member.profileImage.startsWith('http'))
              ? NetworkImage(member.profileImage)
              : null,
          child: (member.profileImage.isEmpty ||
              !member.profileImage.startsWith('http'))
              ? const CircularIcon()
              : null,
        ),
        title: Text(member.name),
        subtitle: isAdminMember ? const Text("Í¥ÄÎ¶¨Ïûê") : null,
        trailing: !isAdminMember && widget.isAdmin
            ? IconButton(
          icon: const Icon(Icons.close, color: Colors.red),
          onPressed: () {
            _showKickDialog(member); // ‚ùå X Î≤ÑÌäº ‚Üí Î™®Îã¨ Ìò∏Ï∂ú
          },
        )
            : null,
      ),
    );
  }

// üëâ Pending Î©§Î≤Ñ ÌÉÄÏùº
  Widget _buildPendingTile(ClubMemberProfile member) {
    return Card(
      color: Colors.transparent,
      elevation: 0,
      child: ListTile(
        leading: CircleAvatar(
          radius: 25,
          backgroundColor: Colors.grey[200],
          backgroundImage: (member.profileImage.isNotEmpty &&
              member.profileImage.startsWith('http'))
              ? NetworkImage(member.profileImage)
              : null,
          child: (member.profileImage.isEmpty ||
              !member.profileImage.startsWith('http'))
              ? const CircularIcon()
              : null,
        ),
        title: Text(member.name),
        subtitle: Text(member.statusType == 'invited' ? "Ï¥àÎåÄÎê®" : "Í∞ÄÏûÖ Ïã†Ï≤≠"),
        trailing: Row(
          mainAxisSize: MainAxisSize.min,
          children: [
            if (member.statusType == 'applied') ...[
              // Í∞ÄÏûÖ Ïã†Ï≤≠Ïûê: ÏäπÏù∏/Í±∞Ï†à Î≤ÑÌäº
              IconButton(
                icon: const Icon(Icons.check, color: Colors.green),
                onPressed: () async {
                  try {
                    await _clubService.approveApplication(widget.clubId, member.accountId);
                    setState(() {
                      pendingMembers.removeWhere((m) => m.memberId == member.memberId);
                      activeMembers.add(member.copyWith(statusType: 'active'));
                    });
                    ScaffoldMessenger.of(context).showSnackBar(
                      SnackBar(
                        content: Text('${member.name}ÎãòÏùò Í∞ÄÏûÖÏùÑ ÏäπÏù∏ÌñàÏäµÎãàÎã§'),
                        backgroundColor: Colors.green,
                      ),
                    );
                  } catch(e) {
                    ScaffoldMessenger.of(context).showSnackBar(
                        SnackBar(
                          content: Text('Í∞ÄÏûÖ ÏäπÏù∏ Ïã§Ìå®: $e'),
                          backgroundColor: Colors.red,
                        ));
                  }
                },
              ),
              IconButton(
                icon: const Icon(Icons.close, color: Colors.red),
                onPressed: () async {
                  try {
                    await _clubService.rejectApplication(widget.clubId, member.accountId);
                    setState(() {
                      pendingMembers.removeWhere((m) => m.memberId == member.memberId);
                    });
                    ScaffoldMessenger.of(context).showSnackBar(
                      SnackBar(
                        content: Text('${member.name}ÎãòÏùò Í∞ÄÏûÖÏùÑ Í±∞Ï†àÌñàÏäµÎãàÎã§'),
                        backgroundColor: Colors.red,
                      ),
                    );
                  } catch(e) {
                    ScaffoldMessenger.of(context).showSnackBar(
                        SnackBar(
                          content: Text('Í∞ÄÏûÖ Í±∞Ï†à Ïã§Ìå®: $e'),
                          backgroundColor: Colors.red,
                        ));
                  }
                },
              ),
            ] else if (member.statusType == 'invited') ...[
              // Ï¥àÎåÄÎêú ÏÇ¨Ïö©Ïûê: Ï∑®ÏÜå Î≤ÑÌäº
              IconButton(
                icon: const Icon(Icons.cancel, color: Colors.orange),
                onPressed: () async {
                  try {
                    await _clubService.cancelInvitation(widget.clubId, member.accountId);
                    setState(() {
                      pendingMembers.removeWhere((m) => m.memberId == member.memberId);
                    });
                    ScaffoldMessenger.of(context).showSnackBar(
                      SnackBar(
                        content: Text('${member.name}ÎãòÏùò Ï¥àÎåÄÎ•º Ï∑®ÏÜåÌñàÏäµÎãàÎã§'),
                        backgroundColor: Colors.orange,
                      ),
                    );
                  } catch(e) {
                    ScaffoldMessenger.of(context).showSnackBar(
                        SnackBar(
                          content: Text('Ï¥àÎåÄ Ï∑®ÏÜå Ïã§Ìå®: $e'),
                          backgroundColor: Colors.red,
                        ));
                  }
                },
              ),
            ],
          ],
        ),
      ),
    );
  }

  /// Î©§Î≤Ñ Ï∂îÎ∞© Îã§Ïù¥ÏñºÎ°úÍ∑∏
  void _showKickDialog(ClubMemberProfile member) {
    showDialog(
      context: context,
      builder: (context) => AlertDialog(
        title: const Text("Î©§Î≤Ñ Ï∂îÎ∞©"),
        content: Text("${member.name}ÎãòÏùÑ Ï∂îÎ∞©ÌïòÏãúÍ≤†ÏäµÎãàÍπå?"),
        actions: [
          TextButton(
            onPressed: () => context.pop(),
            child: const Text("Ï∑®ÏÜå"),
          ),
          ElevatedButton(
            onPressed: () async {
              await _clubService.removeMember(widget.clubId, member.memberId);
              setState(() {
                activeMembers.removeWhere((m) => m.memberId == member.memberId);
              });
              context.pop();
            },
            child: const Text("Ï∂îÎ∞©"),
          ),
        ],
      ),
    );
  }

  /// Î©§Î≤Ñ Ï¥àÎåÄ Îã§Ïù¥ÏñºÎ°úÍ∑∏
  void _showInviteDialog() {
    showDialog(
      context: context,
      builder: (context) {
        return UserDialog(
          selectedUsers: oldMemberUsers, // Ïù¥ÎØ∏ ÏÑ†ÌÉùÎêú Ïú†Ï†Ä
          newSelectedUsers: const [], // ÏÉàÎ°ú Ï¥àÎåÄÌïú Ïú†Ï†Ä
          isAdminMode: false,
        );
      },
    ).then((result) async {
      if (result != null && result.isNotEmpty) {
        final newMemberUsers =
            result.where((e) => !oldMemberUsers.any((old) => old.accountId == e.accountId)).toList();
        final newMembers = await _clubService.inviteMembers(widget.clubId, newMemberUsers);
        setState(() {
          activeMembers.addAll(newMembers.map((m) => m.toProfile().copyWith(statusType: 'active')));
        });
      }
    });
  }
}